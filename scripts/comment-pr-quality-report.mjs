import { existsSync, readFileSync } from 'node:fs';

const marker = '<!-- garfish-wasm-esm-plugin-pr-quality-report -->';

const requiredEnv = {
  GITHUB_TOKEN: process.env.GITHUB_TOKEN,
  GITHUB_REPOSITORY: process.env.GITHUB_REPOSITORY,
  PR_NUMBER: process.env.PR_NUMBER,
  RUN_URL: process.env.RUN_URL,
  HEAD_SHA: process.env.HEAD_SHA,
};

const missing = Object.entries(requiredEnv)
  .filter(([, value]) => !value)
  .map(([key]) => key);

if (missing.length > 0) {
  throw new Error(`Missing required environment variables: ${missing.join(', ')}`);
}

const [owner, repo] = requiredEnv.GITHUB_REPOSITORY.split('/');

if (!owner || !repo) {
  throw new Error('GITHUB_REPOSITORY must be in owner/repo format.');
}

if (!/^[1-9]\d*$/.test(requiredEnv.PR_NUMBER)) {
  throw new Error('PR_NUMBER must be a positive integer.');
}

const shortSha = requiredEnv.HEAD_SHA.slice(0, 7);
const codecovPrUrl = `https://app.codecov.io/gh/${owner}/${repo}/pull/${requiredEnv.PR_NUMBER}`;
const codecovCommitUrl = `https://app.codecov.io/gh/${owner}/${repo}/commit/${requiredEnv.HEAD_SHA}`;

function readBenchmark(path) {
  if (!path || !existsSync(path)) {
    return 'Benchmark results were not generated.';
  }

  return readFileSync(path, 'utf8')
    .replace('<!-- benchmark-results:start -->', '')
    .replace('<!-- benchmark-results:end -->', '')
    .trim();
}

function statusText(status) {
  if (!status) return 'not run';
  return status;
}

const coverageStatus = statusText(process.env.COVERAGE_STATUS);
const codecovStatus = statusText(process.env.CODECOV_STATUS);
const benchmarkStatus = statusText(process.env.BENCHMARK_STATUS);

const body = `${marker}
### Quality report

**Codecov**

- Upload status: \`${codecovStatus}\`
- PR report: [#${requiredEnv.PR_NUMBER}](${codecovPrUrl})
- Commit report: [\`${shortSha}\`](${codecovCommitUrl})
- Coverage details are generated by Codecov's PR comment and status checks.

**Benchmark**

- Benchmark status: \`${benchmarkStatus}\`

${readBenchmark(process.env.BENCHMARK_MARKDOWN_PATH)}

**Workflow**

- Tests with coverage: \`${coverageStatus}\`
- Run: [${process.env.GITHUB_RUN_ID ?? 'workflow'}](${requiredEnv.RUN_URL})
`;

const apiBase = process.env.GITHUB_API_URL || 'https://api.github.com';
const headers = {
  accept: 'application/vnd.github+json',
  authorization: `Bearer ${requiredEnv.GITHUB_TOKEN}`,
  'content-type': 'application/json',
  'x-github-api-version': '2022-11-28',
};

async function githubRequest(path, init = {}) {
  const response = await fetch(`${apiBase}${path}`, {
    ...init,
    headers: {
      ...headers,
      ...init.headers,
    },
  });

  if (!response.ok) {
    const detail = await response.text();
    throw new Error(`GitHub API request failed: ${response.status} ${response.statusText}\n${detail}`);
  }

  if (response.status === 204) {
    return null;
  }

  return response.json();
}

async function listComments() {
  const comments = [];

  for (let page = 1; ; page += 1) {
    const batch = await githubRequest(
      `/repos/${owner}/${repo}/issues/${requiredEnv.PR_NUMBER}/comments?per_page=100&page=${page}`,
    );
    comments.push(...batch);

    if (batch.length < 100) {
      return comments;
    }
  }
}

const comments = await listComments();
const existing = comments.find((comment) => comment.body?.includes(marker));

if (existing) {
  await githubRequest(`/repos/${owner}/${repo}/issues/comments/${existing.id}`, {
    method: 'PATCH',
    body: JSON.stringify({ body }),
  });
  console.log(`Updated PR quality report comment ${existing.id}.`);
} else {
  const comment = await githubRequest(`/repos/${owner}/${repo}/issues/${requiredEnv.PR_NUMBER}/comments`, {
    method: 'POST',
    body: JSON.stringify({ body }),
  });
  console.log(`Created PR quality report comment ${comment.id}.`);
}
